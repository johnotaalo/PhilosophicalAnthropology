					<h4>Types of Freedom</h4>
						<p>Philosophers have distinguished what they call the four dimensions of freedom: fundamental freedom and freedom for choice, which characterize the person as such, along with moral freedom and social freedom which are variable according to the personal and social circumstances.</p>
						<p><strong>Fundamental or constitutive freedom:</strong> A characteristic of the human person as such, by which he is open to the real world, can know and love it and move himself, self-determine himself, to tend towards some good thing freely.</p>
						<ul>
							<li><p>This type of freedom is not a property of the human acts, but rather, it is a property of the whole person, in body and soul with intellect and will; the whole person can tend towards what is good moved by himself (freely).</p></li>
							<li><p>When a person tends towards a good thing, as well as acting personally, he is choosing a good that he wants to possess, make his own; he wants it for himself, to be part of himself, part of his way of being a human person.

						</p></li>
							<li><p>For that reason, we can say that when a person chooses something freely, he is choosing himself, the type of person he wants to be.</p></li>
							<li><p>Thanks to our constitutive freedom as human beings we can develop our personality, and become the type of person we would like to be; each free choice is a choice about ourselves as persons.</p></li>
							<li><p>Freedom involves a risk; we can use our freedom to choose well and really achieve self fulfillment; or we can use our freedom to choose badly and so destroy ourselves.</p></li>
							<li><p>This is because freedom is the power to do with myself, what I like; it is the power which permits me to be myself, achieve personal self-fulfillment or not.

						</p></li>
							<li><p>So the fundamental freedom of the person is inherent in himself, as the capacity to develop oneself, which means that we are in our own hands as the object of our own choices.</p></li>
							<li><p>This fundamental freedom grows and develops if we use it to carry out good free acts that help us actualize ourselves as persons by committing ourselves freely to good things; our personal dimension (spiritual soul, etc) grows as we use our personal capacities well.</p></li>
							<li><p>In this way, we become more human as people and in our acts, and our freedom becomes stronger; capacity to know the truth, love the real good, move self towards real goods becomes stronger, it gets easier to live like this; as we use these capacities in a more perfect way, they develop and we becomes better persons. </p></li>
						</ul>
						<p><strong>Freedom to choose:</strong> Capacity to decide how to act in order to actually determine ourselves, moving ourselves towards a good which will bring us closer to self fulfillment  (actually determine oneself to be oneself in fact).</p>
						<ul>
							<li><p>The existence of this form of freedom is shown in the fact that man needs to choose from various possibilities in order to actually determine himself to move towards some specific good and so advance towards or away from self-realization.</p></li>
							<li><p>Freedom as choice has two aspects: the fact of exercising choice (I decide to choose) and actually choosing some specific thing (I decide to choose this)1.</p></li>
							<li><p>Freedom of choice makes the person actually be the source of specific acts of the will.</p>
								<ul>
									<li><p>When the intellect knows a good thing, the will is still not determined to choose it; no finite good thing actualizes the will in a necessary way.</p></li>
									<li><p>Thanks to freedom (capacity for self determination) the will can always choose to want that good thing and so move towards it, or not to do so.</p></li>
									<li><p>We always have to actually choose in order to actually act in a free way. </p></li>
								</ul>
							</li>
							<li><p>We manifest our freedom in a decision; however, that decision is the fruit of a free choice, which has its roots in the fact that the person wants to commit themselves to that good.</p>
								<ul>
									<li><p>Only the personal self is the cause, source, and owner of that free decision.</p></li>
								</ul>
							</li>
							<li><p>This immanent freedom (made up of constitutive freedom which is actualized in freedom of choice) is what gives dignity and value to human acts, this fact that they are free acts of the person.</p>
								<ul>
									<li><p>To the extent that personal freedom grows through good free acts, the human activity of that person becomes ever better, more dignified.</p></li>
									<li><p>This growing goodness of the person and their life (acts) gives rise to what is known as moral freedom.</p></li>
								</ul>
							</li>
						</ul>
						<p><strong>Moral freedom: </strong>the real, acquired freedom of a human person, achieved due to good self realization through good free acts which have given rise to the personal life project (coherent person and life).</p>
						<ul>
							<li><p>Moral freedom, the acquired freedom of the person and their acts, if it continues growing, leads to the fullness of manâ€™s freedom in his person and his acts.</p></li>
							<li><p>Moral freedom determines the personal dignity of the individual.</p>
								<ul>
									<li><p>Obviously each human being has a basic dignity as a person made of body and soul.</p></li>
									<li><p>However that personal dignity (worth or value) can grow if the person carries out good free acts.</p></li>
									<li><p>As he does this, his moral freedom grows and so his personal dignity grows as he becomes a better person through his acts.</p></li>
								</ul>
							</li>
							<li><p>Moral freedom reflects the personâ€™s capacity to act with ever more and better freedom and so become a better person.</p>
								<ul>
									<li><p>Hence the dignity of the person intensifies as he has strengthened his specifically human activity while growing in moral freedom.</p></li>
								</ul>	
							</li>
							<li><p>For a person to develop moral freedom, and to grow as a person, he needs to carry out a habitually free and good activity.</p>
								<ul>
									<li><p>Moral freedom is gained through good, free acts.</p></li>
								</ul>
							</li>
							<li><p>However, such acts require society to let one do what he wants; there is a need for social freedom, which permits the possibility of each person carrying out their own life project in that society.</p></li>
						</ul>
						<p><strong>Social freedom: </strong>refers to a range of principles expressing from what should simply be allowed to what should actually be fostered in a given society in order to permit the personal self-fulfillment of its members.</p>
						<ul>
							<li><p>It expresses the fact that we need to have the chance to act freely in our world and it includes two elements.</p></li>
							<li><p>Political freedom: let each person be a person and create and develop his strength as a person in the social sphere.</p></li>
							<li><p>Social freedom: a given situation in society that reflects the consequences of using our freedom, which necessarily brings limitations with it because our activities have repercussions in ourselves and in others.</p></li>
						</ul>
