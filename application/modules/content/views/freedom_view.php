					<h4>Nature of Freedom</h4>
						<p>If we stop to think for a moment we will realize that we have all had the experience of freedom; every time we need to choose between one thing and another (one dish or another, one film or another) we are exercising our freedom and we are taking a decision which affects ourselves as persons.</p>
						<p>Freedom is a property of the human being as such; not just the will is free, rather the whole person is free. The whole person will taste the chosen dish, the whole person will watch the chosen movie. Freedom is the characteristic of the human person that permits him to direct his own acts towards attaining a given goal that he has chosen thanks to the fact that he has intellect and will.</p>
						<ul>
							<li><p>Through the intellect the person knows the possible goals and thanks to the will the person can choose to act, and can move freely towards attaining the goal. </p></li>
							<li><p>Man`s freedom lies in the fact that he has intellect and will; because he can know the truth and love the good, he can tend towards attaining the good goal in a free act.</p></li>
							<li><p>Freedom is said to have its root in the intellect, through which we know the end or goal.</p></li>
							<li><p>And the subject of freedom is the will; it is the will that loves and moves to possess the known end; the will moves itself, to love the known goal</p></li>
							<li><p>Through this knowledge and the love of the goal, the free act by which the person moves himself towards the goal can be carried out.</p></li>
						</ul>
						<p>It is due to the will that the person can develop the capacity to love that permits him to move himself towards what seems to be most good.</p>
						<ul>
							<li><p>The human being needs to develop and to strengthen the capacity to love as this makes him freer; it strengthens the energy to move himself towards ever better goals, even if there are difficulties.</p></li>
							<li><p>The development of freedom, then, depends on the strengthening of the will`s capacity to love the good and the intellect`s capacity to know the truth.</p></li>
							<li><p>But above all, it depends on the will because freedom lies in the will when it determines itself, stimulates itself to actually tend towards the known good.</p></li>
							<li><p>As this tending towards the known good is the act of love, we can see that there is a close relationship between freedom (self determination towards the good) and love (the actual tending towards the good).</p></li>
							<li><p>The stronger the person`s capacity to move himself towards the good (the freer he is), the stronger will be his capacity to love (actually tend towards that good).</p></li>
							<li><p>`Here we are touching on the deepest internal objective of the human being: the development of his capacity to love. This development will make him freer, giving him sufficient energy to move himself towards ever more valuable objectives, no matter how great the difficulties and the sacrifices which this may involve. For this reason manâ€™s deepest internal goal is to develop his own freedom, develop this internal energy which will make him capable of resisting any action which may enslave him, no matter how attractive that action may seem to him in a given moment. If man does not develop this capacity to love he will be incapable of choosing goals that will not enslave him more and more. How does this capacity grow in man? Each time the person freely chooses something which he knows is better even though it may seem less attractive than something which is worse, he is training himself, developing the potency capable of freeing him from any pressure which may derail him.`</p></li>
						</ul>
